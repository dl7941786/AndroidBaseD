import com.google.gson.Gson;
import com.google.gson.reflect.TypeToken;
import com.qianfeng.asynctaskdemo.task.MyBaseAsyncTask;

import java.lang.reflect.Type;

/**
 * Created by jackiechan on 16/8/8.
 */
public abstract class MyGsonCallBack<T> implements MyBaseAsyncTask.OnLoadCompleteListener {
    @Override
    public void onLoadComplete(byte[] bs) {
        //1.转成字符串
        String json = new String(bs);
        //解析字符串
        Type type = new TypeToken<T>() {
        }.getType();
        T t = new Gson().fromJson(json, type);
        showData(t);
    }

    public abstract void showData(T t);
}
