import android.view.View;
import android.view.ViewGroup;
import android.view.animation.Animation;
import android.view.animation.AnimationUtils;
import android.widget.BaseAdapter;
import android.widget.TextView;

/**
 * Created by jackiechan on 16/8/23.
 */
public class MyAdapter extends BaseAdapter {
    @Override
    public int getCount() {
        return 100;
    }

    @Override
    public Object getItem(int position) {
        return null;
    }

    @Override
    public long getItemId(int position) {
        return 0;
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        convertView = convertView == null ? new TextView(parent.getContext()) : convertView;
        ((TextView) convertView).setTextSize(30);
        ((TextView) convertView).setText("这是第" + position);
        Animation animation = AnimationUtils.loadAnimation(parent.getContext(), R.anim.itemtranslate);
        convertView.startAnimation(animation);
        return convertView;
    }
}
