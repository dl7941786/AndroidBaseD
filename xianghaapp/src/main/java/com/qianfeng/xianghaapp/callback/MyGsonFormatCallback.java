import com.google.gson.Gson;
import com.qianfeng.xianghaapp.model.MvpModel;

import java.lang.reflect.Type;

/**
 * Created by jackiechan on 16/8/12.
 */
public abstract class MyGsonFormatCallback<T extends Type> implements MvpModel.OnLoadCompleteListener {
    private T t;

    public MyGsonFormatCallback(T t) {
        this.t = t;
    }
    @Override
    public void onLoadComplete(String path, byte[] bytes) {
        String json = new String(bytes);
        //解析数据
        T result= new Gson().fromJson(json, t);
        returnData(result);
        //返回数据
    }

    public abstract void returnData(T t);
}
