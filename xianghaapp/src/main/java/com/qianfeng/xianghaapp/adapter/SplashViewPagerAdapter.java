import android.support.v4.view.PagerAdapter;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ImageView;

import java.util.List;

/**
 * Created by jackiechan on 16/8/24.
 */
public class SplashViewPagerAdapter extends PagerAdapter {
    private List<ImageView> imageViewList;

    public SplashViewPagerAdapter(List<ImageView> imageViewList) {
        this.imageViewList = imageViewList;
        if (imageViewList == null) {
            throw new NullPointerException("没有美女图,抓紧拍点院校美女");
        }
    }

    @Override
    public int getCount() {
        return imageViewList.size();
    }

    @Override
    public boolean isViewFromObject(View view, Object object) {
        return view==object;
    }

    @Override
    public Object instantiateItem(ViewGroup container, int position) {
        ImageView imageView = imageViewList.get(position);
        container.addView(imageView);//加到 viewpager中
        return imageView;//返回的和添加的必须一致,否则不会显示 或者isViewFromObject直接返回 true 才可以
    }

    @Override
    public void destroyItem(ViewGroup container, int position, Object object) {
        container.removeView((View) object);
    }
}
