import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentStatePagerAdapter;

import com.qianfeng.mydriversdemo.view.fragment.BaseFragment;

import java.util.List;

/**
 * Created by jackiechan on 16/8/25.
 */
public class MyFragmentStateAdapter extends FragmentStatePagerAdapter {
    private List<BaseFragment> list;
    private String[] titles;
    public MyFragmentStateAdapter(FragmentManager fm,List<BaseFragment> list,String[] titles) {
        super(fm);
        this.list = list;
        this.titles = titles;
    }

    @Override
    public Fragment getItem(int position) {
        return list.get(position);
    }

    @Override
    public int getCount() {
        return list == null ? 0 : list.size();
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return titles[position];
    }
}
